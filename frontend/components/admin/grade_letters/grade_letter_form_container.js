import { connect } from 'react-redux';
import { createGradeLetter, fetchGradeLetters } from '../../../actions/grade_letter_actions';
import GradeLetterForm from './GradeLetterForm';

const mapStateToProps = (state) => ({
	grade_letters: state.grade_letters,
	currentUser: state.session.currentUser
});

const mapDispatchToProps = dispatch => ({
	createGradeLetter: (data) => dispatch(createGradeLetter(data)),
	fetchGradeLetters: () => dispatch(fetchGradeLetters()),
});

export default connect(
	mapStateToProps,
	mapDispatchToProps
)(GradeLetterForm);